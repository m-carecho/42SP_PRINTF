#include "libft.h"

int	ft_isalnum(int arg)
{
	if ((arg <= 'z' && arg >= 'a') || (arg <= 'Z' && arg >= 'A')
		|| (arg <= '9' && arg >= '0'))
		return (8);
	return (0);
}
